  renderMenu() {
    const t = this.t;
    const catEntries = Object.entries(NIUS_CATEGORIES);

    const tabs = `
      <div class="cat-tabs">
        <button class="cat-tab active" data-filter="all"><i class="ti ti-list"></i>${t.allCategories}</button>
        ${catEntries.map(([catId, cat]) => `
          <button class="cat-tab" data-filter="${catId}"><i class="ti ${cat.icon}"></i>${(t.categories[catId] || {}).label || cat.label}</button>
        `).join('')}
      </div>
    `;

    const cats = catEntries.map(([catId, cat]) => {
      const upsellIds = NIUS_UPSELLS_BY_CAT[catId] || [];
      const catT = t.categories[catId] || {};
      const drips = cat.drips.map((d, i) => this.renderDripCard(d, catId, i, upsellIds)).join('');
      return `
        <div class="cat-block" data-cat="${catId}">
          <div class="cat-label"><i class="ti ${cat.icon}"></i>${catT.label || cat.label}</div>
          <div class="cat-desc">${catT.desc || cat.desc}</div>
          <div class="drip-list">${drips}</div>
        </div>
      `;
    }).join('');

    return `
      <section>
        <div class="section-head">
          <div>
            <div class="sh-label">${t.servicesAndPricing}</div>
            <h2 class="sh-title">${t.menuTitle}</h2>
            <p class="sh-desc">${t.menuDesc}</p>
          </div>
        </div>
        ${tabs}
        ${cats}
      </section>
    `;
  }

